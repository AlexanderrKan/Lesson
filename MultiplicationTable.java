public class MultiplicationTable {
    public static void main(String args[]){
        for (int i = 1; i<10; i++){
            for (int j = 1; j<10; j++){
                System.out.println(i+"*"+j+"="+i*j);
            }
        }

        for (int a = 1; a<8; a++) {
            for (int b = 7; b < 8; b++) {
                System.out.println(a + "*" + b + "=" + a * b);
            }
        }
    }
}
